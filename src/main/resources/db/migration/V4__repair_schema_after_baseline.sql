CREATE TABLE IF NOT EXISTS meal_plans (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description VARCHAR(255) NOT NULL,
    total_calories DOUBLE PRECISION,
    total_carbs DOUBLE PRECISION,
    total_proteins DOUBLE PRECISION,
    total_fats DOUBLE PRECISION,
    profile_id INTEGER,
    category VARCHAR(100) NOT NULL,
    is_current BOOLEAN NOT NULL,
    created_by_nutritionist_user_id INTEGER,
    created_at TIMESTAMP(6) WITH TIME ZONE,
    updated_at TIMESTAMP(6) WITH TIME ZONE
);

CREATE TABLE IF NOT EXISTS meal_plan_types (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type VARCHAR(20)
);

CREATE UNIQUE INDEX IF NOT EXISTS uk_meal_plan_types_type
    ON meal_plan_types (type);

CREATE TABLE IF NOT EXISTS meal_plan_entries (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    recipe_id INTEGER NOT NULL,
    day INTEGER NOT NULL,
    meal_plan_type_id INTEGER NOT NULL,
    meal_plan_id INTEGER NOT NULL
);

CREATE TABLE IF NOT EXISTS meal_plan_tags (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tag VARCHAR(50) NOT NULL,
    meal_plan_id INTEGER
);

DO $$
DECLARE
    seed_type TEXT;
BEGIN
    FOREACH seed_type IN ARRAY ARRAY['Breakfast', 'Lunch', 'Dinner', 'Snack']
    LOOP
        INSERT INTO meal_plan_types (type)
        SELECT seed_type
        WHERE NOT EXISTS (SELECT 1 FROM meal_plan_types WHERE meal_plan_types.type = seed_type);
    END LOOP;
END $$;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_meal_plan_entries_type'
          AND conrelid = 'public.meal_plan_entries'::regclass
    ) THEN
        ALTER TABLE meal_plan_entries
            ADD CONSTRAINT fk_meal_plan_entries_type
            FOREIGN KEY (meal_plan_type_id) REFERENCES meal_plan_types (id);
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_meal_plan_entries_plan'
          AND conrelid = 'public.meal_plan_entries'::regclass
    ) THEN
        ALTER TABLE meal_plan_entries
            ADD CONSTRAINT fk_meal_plan_entries_plan
            FOREIGN KEY (meal_plan_id) REFERENCES meal_plans (id);
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_meal_plan_tags_plan'
          AND conrelid = 'public.meal_plan_tags'::regclass
    ) THEN
        ALTER TABLE meal_plan_tags
            ADD CONSTRAINT fk_meal_plan_tags_plan
            FOREIGN KEY (meal_plan_id) REFERENCES meal_plans (id);
    END IF;
END $$;
